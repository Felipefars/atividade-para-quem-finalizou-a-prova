// Declara as variáveis num1 e num2
let num1 = 200.456;
let num2 = 150.789;

// Arredonda num1 para o valor inteiro mais próximo e exibe no console
console.log(Math.round(num1));

// Encontra o valor máximo entre num1 e num2 e exibe no console
console.log(Math.max(num1, num2));

// Encontra o valor mínimo entre num1 e num2 e exibe no console
console.log(Math.min(num1, num2));

// Calcula a raiz quadrada de num2 e exibe no console
console.log(Math.sqrt(num2));

// Converte num1 para uma string com notação científica e exibe no console
console.log(num1.toExponential());
