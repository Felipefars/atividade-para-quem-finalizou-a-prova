// Declara as variáveis x, y, e z
let x = 8;
let y = 12;
let z = 8;

// Compara se x é maior ou igual a y e exibe o resultado no console
console.log(x >= y);

// Verifica se x é diferente de z (usando !=) e exibe o resultado no console
console.log(x != z);

// Verifica se x é estritamente diferente de z (usando !==) e exibe o resultado no console
console.log(x !== z);

// Usa uma expressão lógica para verificar se x é menor que y e z é maior que x, e exibe o resultado no console
console.log(x < y && z > x);

// Usa uma expressão lógica para verificar se x é igual a z ou y é menor que z, e exibe o resultado no console
console.log(x === z || y < z);
